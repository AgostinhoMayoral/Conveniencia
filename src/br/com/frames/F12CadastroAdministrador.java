    JFileChooser escolha = new JFileChooser();
    private void adicionarImagemAdmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarImagemAdmButtonActionPerformed
        escolha.setDialogTitle("Procurar Foto");
        escolha.setFileSelectionMode(JFileChooser.FILES_ONLY);

        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Fotos", "jpg", "png", "gif");
        escolha.setFileFilter(filtro);

        int resposta = escolha.showOpenDialog(this);

        if (resposta == JFileChooser.APPROVE_OPTION) {

            File arquivoFotos = escolha.getSelectedFile();
            ImageIcon icone = new ImageIcon(arquivoFotos.getPath());
            imagemAdministradorLabel.setIcon(icone);
            imagens.add((ImageIcon) imagemAdministradorLabel.getIcon());
    }//GEN-LAST:event_adicionarImagemAdmButtonActionPerformed
    }
    private void removerImagemAdmButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerImagemAdmButtonActionPerformed
        imagemAdministradorLabel.setIcon(null);
    }//GEN-LAST:event_removerImagemAdmButtonActionPerformed

